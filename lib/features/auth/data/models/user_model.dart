import 'package:cloud_firestore/cloud_firestore.dart';
import '../../domain/entities/user_entity.dart';

class UserModel extends UserEntity {
  UserModel({
    required super.uid,
    required super.name,
    required super.email,
    required super.role,
    super.major,
    super.faculty,
    super.batch,
    super.nimOrEid,
    super.headline,
    super.bio,
    super.location,
    super.profileImageUrl,
    super.bannerImageUrl,
    super.isVerified,
    super.isOpenToWork,
    super.skills,
    super.connections,
    super.followers,
    super.following,
    super.experience,
    super.education,
    super.projects,
    super.certifications,
    super.profileCompletion,
    super.profileViews,
    super.activityStatus,
    super.currentCompany,
    required super.lastActive,
    required super.createdAt,
    super.fcmToken,
    super.settings,
  });

  factory UserModel.fromJson(Map<String, dynamic> json) {
    return UserModel(
      uid: json['uid'] as String,
      name: json['name'] as String,
      email: json['email'] as String,
      role: UserRole.values.firstWhere((e) => e.name == json['role']),
      major: json['major'] as String?,
      faculty: json['faculty'] as String?,
      batch: json['batch'] as int?,
      nimOrEid: json['nim_or_eid'] as String?,
      headline: json['headline'] as String?,
      bio: json['bio'] as String?,
      location: json['location'] as String?,
      profileImageUrl: json['profile_image_url'] as String?,
      bannerImageUrl: json['banner_image_url'] as String?,
      isVerified: json['is_verified'] as bool? ?? false,
      isOpenToWork: json['is_open_to_work'] as bool? ?? false,
      skills: List<String>.from(json['skills'] ?? []),
      connections: List<String>.from(json['connections'] ?? []),
      followers: List<String>.from(json['followers'] ?? []),
      following: List<String>.from(json['following'] ?? []),
      experience: List<Map<String, dynamic>>.from(json['experience'] ?? []),
      education: List<Map<String, dynamic>>.from(json['education'] ?? []),
      projects: List<Map<String, dynamic>>.from(json['projects'] ?? []),
      certifications:
          List<Map<String, dynamic>>.from(json['certifications'] ?? []),
      profileCompletion: (json['profile_completion'] as num?)?.toDouble() ?? 0,
      profileViews: json['profile_views'] as int? ?? 0,
      activityStatus: json['activity_status'] != null
          ? ActivityStatus.values
              .firstWhere((e) => e.name == json['activity_status'])
          : ActivityStatus.student,
      currentCompany: json['current_company'] as String?,
      lastActive: (json['last_active'] as Timestamp).toDate(),
      createdAt: (json['created_at'] as Timestamp).toDate(),
      fcmToken: json['fcm_token'] as String?,
      settings: Map<String, dynamic>.from(json['settings'] ?? {}),
    );
  }

  Map<String, dynamic> toJson() {
    return {
      'uid': uid,
      'name': name,
      'email': email,
      'role': role.name,
      'major': major,
      'faculty': faculty,
      'batch': batch,
      'nim_or_eid': nimOrEid,
      'headline': headline,
      'bio': bio,
      'location': location,
      'profile_image_url': profileImageUrl,
      'banner_image_url': bannerImageUrl,
      'is_verified': isVerified,
      'is_open_to_work': isOpenToWork,
      'skills': skills,
      'connections': connections,
      'followers': followers,
      'following': following,
      'experience': experience,
      'education': education,
      'projects': projects,
      'certifications': certifications,
      'profile_completion': profileCompletion,
      'profile_views': profileViews,
      'activity_status': activityStatus.name,
      'current_company': currentCompany,
      'last_active': Timestamp.fromDate(lastActive),
      'created_at': Timestamp.fromDate(createdAt),
      'fcm_token': fcmToken,
      'settings': settings,
    };
  }
}
